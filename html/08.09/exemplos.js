function fundoAzul(){
    let element = document.querySelector('#style-text')
    element.style.backgroundColor = "blue"
    element.style.border = "2px solid red"
}

function fundoTransparente(){
    let element = document.querySelector('#style-text')
    element.style.backgroundColor = "transparent"

}

function fonteVermelha(){
    let element = document.querySelector('#style-text').style.color ="red";


}

function fonteNormal(){
    let element = document.querySelector('#style-text').style.color ="black";

}